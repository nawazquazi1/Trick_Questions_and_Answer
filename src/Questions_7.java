public class Questions_7 {
    public static void main(String[] args) {
        /*
         * What will be the output when you divide a number by zero
         * Number = int,long,byte
         */
        System.out.println(4.0 / 0);//Infinity
        System.out.println(12.22f / 0);//Infinity
        System.out.println(10 / 0.0);//Infinity
        System.out.println(19.234545 / 0);//Infinity
        System.out.println(0.0 / 0);//Nan
    }
}
